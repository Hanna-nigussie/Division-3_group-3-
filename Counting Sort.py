def countingSort(arr):
    # Initialize a frequency array of size 100 with all zeros
    frequency = [0] * 100
    
    # Count the occurrences of each value in the input array
    for num in arr:
        frequency[num] += 1
    
    # Return the frequency array
    return frequency

